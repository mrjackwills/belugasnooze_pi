#[allow(unused)]
pub enum Color {
    Red,
    BgRed,
    Green,
    Magenta,
    Yellow,
}

impl Color {
    pub fn get(self) -> &'static str {
        match self {
            Color::BgRed => "\x1b[41m",
            Color::Green => "\x1b[32m",
            Color::Magenta => "\x1b[35m",
            Color::Red => "\x1b[31m",
            Color::Yellow => "\x1b[33m",
        }
    }
}

